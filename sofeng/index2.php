                        <h2>History</h2>
 <p>Before the advent of World Citi Medical Center, the prestigious medical hub was named as Quezon City Medical Center - under
 the management of Quezon City Medical Center and Colleges (QCMCC).
 <br> <br>
 It then had a 100-bed capacity which in, circa 70's, was one of the largest hospitals in the area of Quezon City.
 <br> <br>
 Mid 1979, the hospital's Articles of Incorporation was amended, changing its corporate name to Quezon City Medical
 Center and Colleges,Inc. This prompted the founding of its very first college - the College of Nursing.
 <br> <br>
 When the political climate in the Philippines drastically shifted during the 1986 People Power 
 Revolution, so did the management of Quezon City Medical Center.
 <br> <br>
 Newly acquired by the Guico Family of Binalonan in Pangasinan, their inspiring leadership and visionary
 prowess revolutionized the hospital's overall image. The changes paved way for rapid yet consistent growth,
 providing QCMC the niche they long deserve.
 <br> <br>
 As dynamic as the hospital's new management, the two-storey hospital building was developed into 
 a 15-storey edifice spreading its top quality service to hundreds of patients daily.
 <br> <br>
 From a small nursing school, the learning institution dramatically expanded its horizons. Empowered by 
 its vision and armed with a striking mission to bring Filipinos in the forefront of global competency, 
 the Quezon City Medical Center and Colleges was upgraded to World Citi Colleges (WCC).
 <br> <br>
 With the emergence of WCC as one of the most competitive schools in the country, the
 hospital went along with its growth and was restructured into what now is known as the World 
 Citi Medical Center.
 <br> <br>
 World Citi Medical Center became the proponent of Medical Tourism when
 it introduced the Medical-Hotel concept in the country later becoming the first of its kind in the Philippines.
 <br> <br>
 Currently, World CIti Medical Center is a tertiary hospital with level four accreditation by the Department of Health.
</p>
